# --- ‡πÇ‡∏Ñ‡πâ‡∏î‡∏™‡∏µ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏• ---
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
