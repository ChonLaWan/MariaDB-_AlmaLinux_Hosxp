#!/bin/bash

# ==============================================================================
#   ‡∏™‡∏Ñ‡∏£‡∏¥‡∏õ‡∏ï‡πå‡∏õ‡∏£‡∏±‡∏ö‡∏à‡∏π‡∏ô MariaDB 11 ‡πÅ‡∏•‡∏∞ AlmaLinux ‡∏≠‡∏±‡∏ï‡πÇ‡∏ô‡∏°‡∏±‡∏ï‡∏¥
#   - ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö Hardware (RAM, CPU, SSD/HDD)
#   - ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÅ‡∏•‡∏∞‡∏™‡∏£‡πâ‡∏≤‡∏á Config ‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°
#   - ‡∏™‡∏≥‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏Å‡πà‡∏≤‡πÅ‡∏•‡∏∞‡πÉ‡∏™‡πà‡∏Ñ‡∏≥‡∏≠‡∏ò‡∏¥‡∏ö‡∏≤‡∏¢‡∏Ñ‡∏£‡∏ö‡∏ó‡∏∏‡∏Å‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î
#   - ‡∏ï‡πâ‡∏≠‡∏á‡∏£‡∏±‡∏ô‡∏î‡πâ‡∏ß‡∏¢‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå root ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô
# ==============================================================================

# --- 0. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå Root ---
if [[ $EUID -ne 0 ]]; then
   echo "‚ùå ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏±‡∏ô‡∏™‡∏Ñ‡∏£‡∏¥‡∏õ‡∏ï‡πå‡∏ô‡∏µ‡πâ‡∏î‡πâ‡∏ß‡∏¢‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå root ‡∏´‡∏£‡∏∑‡∏≠‡πÉ‡∏ä‡πâ‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á sudo"
   exit 1
fi

echo "‚úÖ ‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô..."
echo ""

# --- 1. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö Hardware ---
echo "üß† ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö Hardware ‡∏Ç‡∏≠‡∏á‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á..."

# ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö RAM (GB) ‡πÅ‡∏•‡∏∞ CPU Cores
TOTAL_RAM_GB=$(free -g | awk '/^Mem:/ {print $2}')
CPU_CORES=$(nproc)
echo "   - ‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö RAM: ${TOTAL_RAM_GB} GB"
echo "   - ‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö CPU: ${CPU_CORES} Cores"

# ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ä‡∏ô‡∏¥‡∏î‡∏Ç‡∏≠‡∏á‡∏î‡∏¥‡∏™‡∏Å‡πå (SSD ‡∏´‡∏£‡∏∑‡∏≠ HDD)
DATA_DIR_DEVICE=$(df /var/lib/mysql | awk 'NR==2 {print $1}')
if [[ -z "$DATA_DIR_DEVICE" ]]; then
    echo "‚ö†Ô∏è ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏´‡∏≤ Device ‡∏ó‡∏µ‡πà‡∏ï‡∏¥‡∏î‡∏ï‡∏±‡πâ‡∏á /var/lib/mysql ‡πÑ‡∏î‡πâ"
    DISK_TYPE="UNKNOWN"
else
    # ‡∏´‡∏≤‡∏ä‡∏∑‡πà‡∏≠ Device ‡∏´‡∏•‡∏±‡∏Å (‡πÄ‡∏ä‡πà‡∏ô sda ‡∏à‡∏≤‡∏Å sda1)
    DISK_NAME=$(lsblk -no pkname "$DATA_DIR_DEVICE" | head -n 1)
    if [[ -z "$DISK_NAME" ]]; then
        DISK_NAME=$(lsblk -no name "$DATA_DIR_DEVICE" | sed 's/.*\///' | head -n 1 | tr -d '0-9')
    fi
    
    # ‡∏≠‡πà‡∏≤‡∏ô‡∏Ñ‡πà‡∏≤ rotational
    ROTATIONAL=$(cat /sys/block/${DISK_NAME}/queue/rotational 2>/dev/null)

    if [[ "$ROTATIONAL" == "0" ]]; then
        DISK_TYPE="SSD"
        echo "   - ‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö Disk Type: SSD (Non-rotational)"
    else
        DISK_TYPE="HDD"
        echo "   - ‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö Disk Type: HDD/SAS (Rotational)"
    fi
fi
echo ""


# --- 2. ‡∏™‡∏≥‡∏£‡∏≠‡∏á‡πÑ‡∏ü‡∏•‡πå Config ‡πÄ‡∏î‡∏¥‡∏° ---
BACKUP_DIR="/root/config_backup_$(date +%Y-%m-%d_%H%M%S)"
echo "üõ°Ô∏è ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏™‡∏≥‡∏£‡∏≠‡∏á‡πÑ‡∏ü‡∏•‡πå Config ‡πÑ‡∏õ‡∏ó‡∏µ‡πà ${BACKUP_DIR}..."
mkdir -p "$BACKUP_DIR"

if [ -f /etc/my.cnf ]; then
    cp /etc/my.cnf "${BACKUP_DIR}/my.cnf.bak"
    echo "   - ‡∏™‡∏≥‡∏£‡∏≠‡∏á /etc/my.cnf ‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢"
fi
if [ -f /etc/sysctl.conf ]; then
    cp /etc/sysctl.conf "${BACKUP_DIR}/sysctl.conf.bak"
    echo "   - ‡∏™‡∏≥‡∏£‡∏≠‡∏á /etc/sysctl.conf ‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢"
fi
echo ""


# --- 3. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤ Config ‡∏ï‡∏≤‡∏° Hardware ---
echo "üßÆ ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤ Config ‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°..."

# ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì InnoDB Buffer Pool Size (‡∏õ‡∏£‡∏∞‡∏°‡∏≤‡∏ì 65% ‡∏Ç‡∏≠‡∏á RAM)
BUFFER_POOL_SIZE=$(echo "scale=0; ${TOTAL_RAM_GB} * 0.65 / 1" | bc)G

# ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì InnoDB Buffer Pool Instances (‡∏õ‡∏£‡∏∞‡∏°‡∏≤‡∏ì 1 instance ‡∏ï‡πà‡∏≠ 1GB, ‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î 64)
BUFFER_POOL_INSTANCES=$(echo "scale=0; ${BUFFER_POOL_SIZE%G} / 1" | bc)
if (( BUFFER_POOL_INSTANCES < 1 )); then BUFFER_POOL_INSTANCES=1; fi
if (( BUFFER_POOL_INSTANCES > 64 )); then BUFFER_POOL_INSTANCES=64; fi

# ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì Thread & IO Threads (‡∏õ‡∏£‡∏±‡∏ö‡∏ï‡∏≤‡∏°‡∏à‡∏≥‡∏ô‡∏ß‡∏ô Core)
if (( CPU_CORES >= 32 )); then
    READ_IO_THREADS=16
    WRITE_IO_THREADS=16
    THREAD_CACHE_SIZE=128
elif (( CPU_CORES >= 16 )); then
    READ_IO_THREADS=8
    WRITE_IO_THREADS=8
    THREAD_CACHE_SIZE=64
else
    READ_IO_THREADS=4
    WRITE_IO_THREADS=4
    THREAD_CACHE_SIZE=32
fi

# ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö HugePages
SHMMAX=$(echo "scale=0; ${TOTAL_RAM_GB} * 1024 * 1024 * 1024 * 0.85 / 1" | bc)
SHMALL=$(echo "scale=0; ${SHMMAX} / 4096" | bc)
NR_HUGEPAGES=$(echo "scale=0; ${BUFFER_POOL_SIZE%G} * 1024 / 2" | bc)

echo "   - InnoDB Buffer Pool Size: ${BUFFER_POOL_SIZE}"
echo "   - InnoDB Read/Write IO Threads: ${READ_IO_THREADS}"
echo ""


# --- 4. ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÑ‡∏ü‡∏•‡πå Kernel Tuning (`sysctl.conf`) ---
echo "‚úçÔ∏è ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô‡πÑ‡∏ü‡∏•‡πå /etc/sysctl.conf..."
