            log_warn "‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ñ‡πà‡∏≤ '$key' ‡πÉ‡∏ô $file, ‡∏ó‡∏≥‡∏Å‡∏≤‡∏£‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ñ‡πà‡∏≤‡πÉ‡∏´‡∏°‡πà‡πÉ‡∏ï‡πâ [mysqld]"
        else
            echo -e "\n[mysqld]\n${key} = ${value}" | sudo tee -a "$file" > /dev/null
