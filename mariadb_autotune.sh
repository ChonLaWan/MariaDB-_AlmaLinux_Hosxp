EOF
else
    log_info "‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡πà‡∏≤‡πÉ‡∏ô /etc/sysctl.conf..."
    update_config "/etc/sysctl.conf" "kernel.shmmax" "${SHMMAX}"
    update_config "/etc/sysctl.conf" "kernel.shmall" "${SHMALL}"
    update_config "/etc/sysctl.conf" "vm.nr_hugepages" "${HUGEPAGES}"
