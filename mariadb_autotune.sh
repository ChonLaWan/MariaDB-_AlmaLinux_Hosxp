        sudo firewall-cmd --zone=public --add-port=${DB_PORT}/tcp --permanent
        sudo firewall-cmd --reload
        log_info "‡πÄ‡∏õ‡∏¥‡∏î Port ${DB_PORT} ‡πÉ‡∏ô Firewall ‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢"
    else
        log_info "Port ${DB_PORT} ‡∏ñ‡∏π‡∏Å‡πÄ‡∏õ‡∏¥‡∏î‡πÉ‡∏ô Firewall ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß"
    fi
else
    log_warn "‡πÑ‡∏°‡πà‡∏û‡∏ö Firewalld ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô, ‡∏Ç‡πâ‡∏≤‡∏°‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏Å‡∏≤‡∏£‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ Firewall"
