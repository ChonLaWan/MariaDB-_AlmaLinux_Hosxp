# --- ‡∏ô‡∏µ‡πà‡∏Ñ‡∏∑‡∏≠‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡πÉ‡∏ä‡πâ‡∏™‡∏π‡∏ï‡∏£‡πÅ‡∏ï‡∏Å‡∏ï‡πà‡∏≤‡∏á‡∏Å‡∏±‡∏ô‡∏ï‡∏≤‡∏°‡∏Ç‡∏ô‡∏≤‡∏î RAM (Conservative Logic) ---
log_info "‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì Buffer Pool ‡∏ï‡∏≤‡∏°‡∏Ç‡∏ô‡∏≤‡∏î RAM ‡∏ó‡∏µ‡πà‡πÅ‡∏ï‡∏Å‡∏ï‡πà‡∏≤‡∏á‡∏Å‡∏±‡∏ô..."
if [ "$TOTAL_RAM_GB" -lt 32 ]; then
    PERCENTAGE=50 # ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö RAM ‡∏ô‡πâ‡∏≠‡∏¢‡∏Å‡∏ß‡πà‡∏≤ 32GB, ‡πÉ‡∏ä‡πâ‡∏™‡∏π‡∏ï‡∏£‡∏ó‡∏µ‡πà‡∏õ‡∏•‡∏≠‡∏î‡∏†‡∏±‡∏¢‡∏°‡∏≤‡∏Å
elif [ "$TOTAL_RAM_GB" -lt 128 ]; then
    PERCENTAGE=70 # ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö RAM 32GB - 127GB
else
    PERCENTAGE=80 # ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö RAM 128GB+
fi
log_info "‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏ö RAM ${TOTAL_RAM_GB}GB, ‡πÉ‡∏ä‡πâ‡∏™‡∏π‡∏ï‡∏£ ${PERCENTAGE}% ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Buffer Pool"
INNODB_BUFFER_POOL_GB=$((TOTAL_RAM_GB * PERCENTAGE / 100))
[ "$INNODB_BUFFER_POOL_GB" -lt 2 ] && INNODB_BUFFER_POOL_GB=2

SHMMAX=$((TOTAL_RAM_GB * 1024 * 1024 * 1024 * 90 / 100))
SHMALL=$((SHMMAX / 4096))
HUGEPAGES=$((INNODB_BUFFER_POOL_GB * 1024 / 2))

INNODB_LOG_FILE_SIZE_MB=$((INNODB_BUFFER_POOL_GB * 1024 / 4))
if [ "$INNODB_LOG_FILE_SIZE_MB" -gt 4096 ]; then INNODB_LOG_FILE_SIZE="4G"; \
elif [ "$INNODB_LOG_FILE_SIZE_MB" -lt 512 ]; then INNODB_LOG_FILE_SIZE="512M"; \
else INNODB_LOG_FILE_SIZE="${INNODB_LOG_FILE_SIZE_MB}M"; fi

log_info "‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡∏à‡∏∞‡πÉ‡∏ä‡πâ: innodb_buffer_pool_size=${INNODB_BUFFER_POOL_GB}G, innodb_log_file_size=${INNODB_LOG_FILE_SIZE}, vm.nr_hugepages=${HUGEPAGES}"
sleep 2

# ==================================
# ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 5: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÅ‡∏•‡∏∞‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ Systemd Limits
# ==================================
log_info "‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 5: ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÅ‡∏•‡∏∞‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ Systemd Limits..."
