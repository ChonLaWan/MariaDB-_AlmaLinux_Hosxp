echo "‚úçÔ∏è ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ô‡πÑ‡∏ü‡∏•‡πå Systemd Override ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö MariaDB..."
