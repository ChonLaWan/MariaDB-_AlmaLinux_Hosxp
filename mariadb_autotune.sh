echo "‚úçÔ∏è ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÑ‡∏ü‡∏•‡πå /etc/my.cnf ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏õ‡∏£‡∏±‡∏ö‡∏à‡∏π‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö ${DISK_TYPE}..."
