    INNODB_LOG_FILE_SIZE="4G"
elif [ "$INNODB_LOG_FILE_SIZE_MB" -lt 512 ]; then
    INNODB_LOG_FILE_SIZE="512M"
else
    INNODB_LOG_FILE_SIZE="${INNODB_LOG_FILE_SIZE_MB}M"
fi

log_info "‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡∏à‡∏∞‡πÉ‡∏ä‡πâ‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏±‡∏ö‡∏à‡∏π‡∏ô:"
